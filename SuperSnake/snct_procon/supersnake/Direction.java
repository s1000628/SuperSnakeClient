package snct_procon.supersnake;

/**
 * 8方向の向きを表す.
 */
public enum Direction {
    /**
     * 右.
     */
    RIGHT,
    
    /**
     * 右上.
     */
    RIGHT_UP,
    
    /**
     * 上.
     */
    UP,
    
    /**
     * 左上.
     */
    LEFT_UP,
    
    /**
     * 左.
     */
    LEFT,
    
    /**
     * 左下.
     */
    LEFT_DOWN,
    
    /**
     * 下.
     */
    DOWN,
    
    /**
     * 右下.
     */
    RIGHT_DOWN,
}
